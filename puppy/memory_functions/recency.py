class R_ConstantInitialization:
    """
    기억의 '최신성(Recency)' 점수를 초기화하는 클래스입니다.

    '최신성'은 정보가 얼마나 최신인지를 나타내는 점수입니다.
    이 프로젝트에서는 새로운 기억이 추가될 때, 그 최신성 점수를 항상
    최대값인 1.0으로 설정하는 간단한 방식을 사용합니다.

    시간이 지남에 따라 이 점수는 'decay' 함수에 의해 점차 감소하게 됩니다.
    """
    def __call__(self) -> float:
        """
        이 클래스의 인스턴스가 함수처럼 호출될 때, 항상 1.0을 반환합니다.

        :return: 초기 최신성 점수인 1.0
        """
        return 1.0
